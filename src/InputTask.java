
public class InputTask{

    String methodName;
    String argument1;
    String argument2;
    int zoneId;


    public InputTask() {

    }

    public InputTask(String methodName, String argument1,String argument2, int zoneId){
        this.methodName = methodName;
        this.argument1 = argument1;
        this.argument2 = argument2;
        this.zoneId = zoneId;
    }
}